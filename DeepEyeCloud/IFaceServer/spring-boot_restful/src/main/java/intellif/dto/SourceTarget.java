package intellif.dto;

public class SourceTarget {
	private int id = 0;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	private String name = "";

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	private String image = "";

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
}
